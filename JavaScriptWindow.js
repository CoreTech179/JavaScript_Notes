// BOM AND DOM

// Window Vs DOM: 
// 1. Window is the main character or we can say that global object. Any operations related to entire browser window can be a part of the window object. Whereas DOM is the child of window object

// 2. All the members like objects, methods or properties. If they are the part of the Window object then we do not have to write "Window.someThing". We can directly use the methods or the Properties. 
// Whereas we need to refer the document, if we want to use the document object, methods or properties. (i.e. document.someThing ) 


// 3. The DOM (Document Object Models) is focused on the content of the Page only and BOM (Browser Object Models) is focused on the browser environment only.
